
### Programación Tradicional:
# Usa funciones sueltas sin una estructura organizada en clases.
# Es más sencilla y rápida para programas pequeños.
# La lógica está separada pero no encapsulada.
# Menos reutilizable y difícil de escalar.

### Programación Orientada a Objetos (POO):
# Utiliza clases y objetos para estructurar el programa.
#  Permite encapsular los datos y las funciones que los manipulan.
# Más organizada, reutilizable y mantenible.
#  Adecuada para aplicaciones más complejas o que evolucionarán con el paso del tiempo
